#include <stdio.h>
#include <stdlib.h>
#include <readline/readline.h>
#include <readline/history.h>

int	main(void)
{
	char	*line;

	using_history();
	while (1)
	{
		line = readline("> ");
		if (!line)
			break ;
		if (line != "")
			add_history(line);
		printf("%s\n", line);
		free(line);
	}
	clear_history();
	return (0);
}
